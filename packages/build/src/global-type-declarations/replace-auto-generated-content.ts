import { writeFile } from "fs/promises";
import { AUTO_GENERATED_MARKER } from "./constants.js";

/**
 * Error types for better error handling
 */
export enum FileProcessingError {
  FILE_NOT_FOUND = "FILE_NOT_FOUND",
  PERMISSION_DENIED = "PERMISSION_DENIED",
  INVALID_PATH = "INVALID_PATH",
  READ_ERROR = "READ_ERROR",
  WRITE_ERROR = "WRITE_ERROR"
}

/**
 * Custom error class for file processing operations
 */
export class FileProcessingException extends Error {
  constructor(
    public readonly errorType: FileProcessingError,
    message: string,
    public readonly originalError?: Error
  ) {
    super(message);
    this.name = "FileProcessingException";
  }
}

/**
 * Options for the file processing function
 */
export interface ProcessFileOptions {
  /** File encoding (default: 'utf8') */
  encoding?: BufferEncoding;
  /** Whether to create the file if it doesn't exist (default: true) */
  createIfNotExists?: boolean;
  /** Line ending style (default: auto-detect from file, fallback to '\n') */
  lineEnding?: "\n" | "\r\n" | "auto";
}

/**
 * Detects the line ending style used in a file
 * Returns the detected line ending or the provided fallback
 */
function detectLineEnding(
  content: string,
  fallback: "\n" | "\r\n" = "\n"
): "\n" | "\r\n" {
  if (content.includes("\r\n")) {
    return "\r\n";
  }
  if (content.includes("\n")) {
    return "\n";
  }
  return fallback;
}

/**
 * Result of the file processing operation
 */
export interface ProcessFileResult {
  wasUpdated: boolean;
}

/**
 * Processes a file by either replacing content after an auto-generated marker
 * or appending the marker and content if not found.
 *
 * @param filePath - Absolute or relative path to the file
 * @param newAutoGeneratedContent - Content to insert after the auto-generated marker
 * @param options - Configuration options for the operation
 * @returns Promise with operation result details
 *
 * @throws {FileProcessingException} When file operations fail
 *
 * @example
 * ```typescript
 * try {
 *   const result = await processAutoGeneratedFile(
 *     './src/types.ts',
 *     'export interface NewType { id: string; }'
 *   );
 *   console.log(`Processed ${result.filePath}, marker found: ${result.markerFound}`);
 * } catch (error) {
 *   if (error instanceof FileProcessingException) {
 *     console.error(`File processing failed: ${error.errorType} - ${error.message}`);
 *   }
 * }
 * ```
 */
export async function writeGlobalTypesForComponent(
  filePath: string,
  originalFileContent: string,
  newAutoGeneratedContent: string,
  options: ProcessFileOptions = {}
): Promise<ProcessFileResult> {
  if (
    newAutoGeneratedContent === null ||
    newAutoGeneratedContent === undefined
  ) {
    throw new FileProcessingException(
      FileProcessingError.INVALID_PATH,
      "[kasstor] New content cannot be null or undefined"
    );
  }

  // Set default options
  const { lineEnding: providedLineEnding = "auto" } = options;

  try {
    // Check if file exists and is accessible

    // Determine the line ending to use
    // If "auto", detect from original file content; otherwise use provided value
    const lineEnding =
      providedLineEnding === "auto"
        ? detectLineEnding(originalFileContent, "\n")
        : providedLineEnding;

    // Process the content
    const processedContent = processFileContent(
      originalFileContent,
      newAutoGeneratedContent,
      lineEnding
    );

    // Compare contents to determine if file needs to be written
    // Normalize both for comparison: same line endings and trim trailing whitespace per line
    const normalizeForComparison = (content: string): string => {
      return content
        .replace(/\r\n|\r|\n/g, lineEnding)
        .split(lineEnding)
        .map(line => line.trimEnd())
        .join(lineEnding);
    };

    const normalizedOriginal = normalizeForComparison(originalFileContent);
    const normalizedProcessed = normalizeForComparison(
      processedContent.newContent
    );

    const needsToUpdate = normalizedOriginal !== normalizedProcessed;

    if (needsToUpdate) {
      try {
        await writeFile(filePath, processedContent.newContent, "utf-8");
      } catch (writeError) {
        throw new FileProcessingException(
          FileProcessingError.WRITE_ERROR,
          `[kasstor] Failed to write file: ${filePath}`,
          writeError as Error
        );
      }
    }

    // Return operation result
    return {
      wasUpdated: needsToUpdate
    };
  } catch (error) {
    // Re-throw FileProcessingException as-is
    if (error instanceof FileProcessingException) {
      throw error;
    }

    // Wrap other errors
    throw new FileProcessingException(
      FileProcessingError.READ_ERROR,
      `[kasstor] Unexpected error processing file: ${filePath}`,
      error as Error
    );
  }
}

/**
 * Internal function to process file content logic
 * Separated for better testability and single responsibility
 */
function processFileContent(
  originalContent: string,
  newAutoGeneratedContent: string,
  lineEnding: string
): {
  newContent: string;
} {
  // Normalize line endings in original content
  const normalizedOriginal = originalContent.replace(/\r\n|\r|\n/g, lineEnding);
  const lines = normalizedOriginal ? normalizedOriginal.split(lineEnding) : [];

  // Find the marker line index
  const markerIndex = lines.findIndex(
    line => line.trim() === AUTO_GENERATED_MARKER.trim()
  );

  let finalContent: string;
  const markerFound = markerIndex !== -1;

  if (markerFound) {
    // Marker found: preserve content before marker, replace content after
    const contentBeforeMarker = lines.slice(0, markerIndex + 1);

    finalContent =
      contentBeforeMarker.join(lineEnding) +
      lineEnding +
      newAutoGeneratedContent;
  } else {
    // Marker not found: append marker and content
    // Determine if we need to add a line break before the marker
    const needsLineBreak =
      normalizedOriginal && !normalizedOriginal.endsWith(lineEnding);
    const prefix = needsLineBreak ? lineEnding : "";

    finalContent =
      normalizedOriginal +
      prefix +
      lineEnding +
      AUTO_GENERATED_MARKER +
      lineEnding +
      newAutoGeneratedContent;
  }

  return {
    newContent: finalContent
  };
}

