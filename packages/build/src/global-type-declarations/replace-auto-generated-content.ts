import { constants } from "fs";
import { access, readFile, writeFile } from "fs/promises";
import { resolve } from "path";
import { styleText } from "util";
import { AUTO_GENERATED_MARKER } from "./constants.js";

/**
 * Error types for better error handling
 */
export enum FileProcessingError {
  FILE_NOT_FOUND = "FILE_NOT_FOUND",
  PERMISSION_DENIED = "PERMISSION_DENIED",
  INVALID_PATH = "INVALID_PATH",
  READ_ERROR = "READ_ERROR",
  WRITE_ERROR = "WRITE_ERROR"
}

/**
 * Custom error class for file processing operations
 */
export class FileProcessingException extends Error {
  constructor(
    public readonly errorType: FileProcessingError,
    message: string,
    public readonly originalError?: Error
  ) {
    super(message);
    this.name = "FileProcessingException";
  }
}

/**
 * Options for the file processing function
 */
export interface ProcessFileOptions {
  /** File encoding (default: 'utf8') */
  encoding?: BufferEncoding;
  /** Whether to create the file if it doesn't exist (default: true) */
  createIfNotExists?: boolean;
  /** Line ending style (default: '\n') */
  lineEnding?: "\n" | "\r\n";
}

/**
 * Result of the file processing operation
 */
export interface ProcessFileResult {
  /** Whether the marker was found in the original file */
  markerFound: boolean;
  /** Number of lines that were preserved before the marker */
  preservedLines: number;
  /** Total lines in the final file */
  totalLines: number;
  /** Path of the processed file */
  filePath: string;
}

/**
 * Processes a file by either replacing content after an auto-generated marker
 * or appending the marker and content if not found.
 *
 * @param filePath - Absolute or relative path to the file
 * @param newAutoGeneratedContent - Content to insert after the auto-generated marker
 * @param options - Configuration options for the operation
 * @returns Promise with operation result details
 *
 * @throws {FileProcessingException} When file operations fail
 *
 * @example
 * ```typescript
 * try {
 *   const result = await processAutoGeneratedFile(
 *     './src/types.ts',
 *     'export interface NewType { id: string; }'
 *   );
 *   console.log(`Processed ${result.filePath}, marker found: ${result.markerFound}`);
 * } catch (error) {
 *   if (error instanceof FileProcessingException) {
 *     console.error(`File processing failed: ${error.errorType} - ${error.message}`);
 *   }
 * }
 * ```
 */
export async function processAutoGeneratedFile(
  filePath: string,
  newAutoGeneratedContent: string,
  options: ProcessFileOptions = {}
): Promise<ProcessFileResult> {
  // Validate input parameters
  if (!filePath?.trim()) {
    throw new FileProcessingException(
      FileProcessingError.INVALID_PATH,
      "File path cannot be empty or null"
    );
  }

  if (
    newAutoGeneratedContent === null ||
    newAutoGeneratedContent === undefined
  ) {
    throw new FileProcessingException(
      FileProcessingError.INVALID_PATH,
      "New content cannot be null or undefined"
    );
  }

  // Set default options
  const {
    encoding = "utf8",
    createIfNotExists = true,
    lineEnding = "\n"
  } = options;

  // Resolve the file path to absolute path
  const absolutePath = resolve(filePath);

  try {
    // Check if file exists and is accessible
    let originalFileContent = "";

    try {
      await access(
        absolutePath,
        constants.F_OK | constants.R_OK | constants.W_OK
      );
      originalFileContent = await readFile(absolutePath, encoding);
    } catch (accessError) {
      if (!createIfNotExists) {
        throw new FileProcessingException(
          FileProcessingError.FILE_NOT_FOUND,
          `File not found: ${absolutePath}`,
          accessError as Error
        );
      }
    }

    // Process the content
    const processedContent = processFileContent(
      originalFileContent,
      newAutoGeneratedContent,
      lineEnding
    );

    // Write the processed content back to file, if it differs from the
    // original. We must check this to avoid issues in VSCode when the files
    // are not saved, but saving this new content will prompt an alert to
    // override the content when the user tries to save the file
    if (originalFileContent !== processedContent.newContent) {
      try {
        await writeFile(absolutePath, processedContent.newContent, encoding);

        console.log(
          styleText("cyanBright", "  Updated auto-generated content for:"),
          filePath
            .replace(process.cwd(), "")
            .replaceAll("\\", "/")
            .replace(/^\//, "./")
        );
      } catch (writeError) {
        throw new FileProcessingException(
          FileProcessingError.WRITE_ERROR,
          `Failed to write file: ${absolutePath}`,
          writeError as Error
        );
      }
    }

    // Return operation result
    return {
      markerFound: processedContent.markerFound,
      preservedLines: processedContent.preservedLines,
      totalLines: processedContent.newContent.split(lineEnding).length,
      filePath: absolutePath
    };
  } catch (error) {
    // Re-throw FileProcessingException as-is
    if (error instanceof FileProcessingException) {
      throw error;
    }

    // Wrap other errors
    throw new FileProcessingException(
      FileProcessingError.READ_ERROR,
      `Unexpected error processing file: ${absolutePath}`,
      error as Error
    );
  }
}

/**
 * Internal function to process file content logic
 * Separated for better testability and single responsibility
 */
function processFileContent(
  originalContent: string,
  newAutoGeneratedContent: string,
  lineEnding: string
): {
  newContent: string;
  markerFound: boolean;
  preservedLines: number;
} {
  // Normalize line endings in original content
  const normalizedOriginal = originalContent.replace(/\r\n|\r|\n/g, lineEnding);
  const lines = normalizedOriginal ? normalizedOriginal.split(lineEnding) : [];

  // Find the marker line index
  const markerIndex = lines.findIndex(
    line => line.trim() === AUTO_GENERATED_MARKER.trim()
  );

  let finalContent: string;
  let preservedLines: number;
  const markerFound = markerIndex !== -1;

  if (markerFound) {
    // Marker found: preserve content before marker, replace content after
    const contentBeforeMarker = lines.slice(0, markerIndex + 1);
    preservedLines = contentBeforeMarker.length - 1; // Exclude marker line

    finalContent =
      contentBeforeMarker.join(lineEnding) +
      lineEnding +
      newAutoGeneratedContent;
  } else {
    // Marker not found: append marker and content
    preservedLines = lines.length;

    // Determine if we need to add a line break before the marker
    const needsLineBreak =
      normalizedOriginal && !normalizedOriginal.endsWith(lineEnding);
    const prefix = needsLineBreak ? lineEnding : "";

    finalContent =
      normalizedOriginal +
      prefix +
      lineEnding +
      AUTO_GENERATED_MARKER +
      lineEnding +
      newAutoGeneratedContent;
  }

  return {
    newContent: finalContent,
    markerFound,
    preservedLines
  };
}

/**
 * Utility function to check if a file contains the auto-generated marker
 * Useful for validation before processing
 */
export async function hasAutoGeneratedMarker(
  filePath: string,
  encoding: BufferEncoding = "utf8"
): Promise<boolean> {
  try {
    const content = await readFile(resolve(filePath), encoding);
    return content.includes(AUTO_GENERATED_MARKER);
  } catch {
    return false;
  }
}

/**
 * Utility function to extract content before the auto-generated marker
 * Useful for backup or analysis purposes
 */
export async function extractPreservedContent(
  filePath: string,
  encoding: BufferEncoding = "utf8"
): Promise<string | null> {
  try {
    const content = await readFile(resolve(filePath), encoding);
    const lines = content.split(/\r\n|\r|\n/);
    const markerIndex = lines.findIndex(
      line => line.trim() === AUTO_GENERATED_MARKER.trim()
    );

    if (markerIndex === -1) {
      return content; // No marker found, return entire content
    }

    return lines.slice(0, markerIndex).join("\n");
  } catch {
    return null;
  }
}

