import { describe, expect, it } from "vitest";

import { stripAutoGeneratedContent } from "../clean-auto-generated-content.js";
import { AUTO_GENERATED_MARKER } from "../constants.js";

const componentSource = `import { x } from "y";

export class Foo {
  bar = 1;
}`;

const withAutoGenerated = (lineEnding: "\n" | "\r\n" = "\n") =>
  [
    componentSource,
    "",
    AUTO_GENERATED_MARKER,
    "",
    "declare global {",
    "  interface HTMLFooElement {}",
    "}",
    ""
  ]
    .join("\n")
    .replace(/\n/g, lineEnding);

describe("stripAutoGeneratedContent", () => {
  it("strips the marker + declare global block, preserving the source", () => {
    expect(stripAutoGeneratedContent(withAutoGenerated())).toBe(componentSource + "\n");
  });

  it("preserves the original line ending (CRLF)", () => {
    const result = stripAutoGeneratedContent(withAutoGenerated("\r\n"));

    expect(result).toBe(componentSource.replace(/\n/g, "\r\n") + "\r\n");
  });

  it("returns null when there is no marker (idempotent)", () => {
    expect(stripAutoGeneratedContent(componentSource + "\n")).toBeNull();
  });

  it("is idempotent: stripping an already-clean result returns null", () => {
    const stripped = stripAutoGeneratedContent(withAutoGenerated());

    expect(stripped).not.toBeNull();
    expect(stripAutoGeneratedContent(stripped!)).toBeNull();
  });

  it("does not touch a marker that is not followed by `declare global`", () => {
    const content = [
      componentSource,
      "",
      AUTO_GENERATED_MARKER,
      "",
      "const handWritten = 1;",
      ""
    ].join("\n");

    expect(stripAutoGeneratedContent(content)).toBeNull();
  });
});
