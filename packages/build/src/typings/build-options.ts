import type { ComponentDefinition } from "./library-components";

export type KasstorBuildOptions = {
  customComponentDecoratorNames?: string[];

  defaultComponentAccess?: ComponentDefinition["access"];

  /**
   * Paths or patterns to exclude when searching for components.
   *
   * If a path or pattern is specified, components matching these will be
   * excluded.
   *
   * Exclusions take precedence over inclusions.
   */
  excludedPaths?: RegExp | RegExp[];

  excludedPublicMethods?: string[];

  /**
   * Options related to file generation during the build process.
   *
   * These options will auto-generate and update certain files as part of the
   * build, with the following objectives:
   *   - Improve the type safety of components.
   *   - Add support for using the component in different frameworks/libraries
   *     (React, SolidJS, StencilJS, etc.).
   *   - Provide a summary of the library to generate showcase, analytics, or
   *     even more advanced features (like providing data for LLMs).
   */
  fileGeneration?:
    | false
    | {
        /**
         * Adds support to generate a file which exports all types definition
         * used by the properties, events, methods of components.
         *
         * This file is crucial to facilitate the usage of components when
         * creating a library, because it provides a unique entry points for
         * all possible types used by the components.
         *
         *  - If `false`, the file won't be generated.
         *  - If a string is provided, it will be used as the name of the file.
         *  - If no value is provided, the file will be generated by default
         *    with the name "component.ts"
         */
        exportTypesForTheLibrary?: string | false;

        librarySummary?: string | false;

        /**
         * Generates one declaration file per type used by properties, events,
         * and methods. Types are merged from propertyImportTypes,
         * eventImportTypes, and methodImportTypes (duplicate type names are
         * emitted only once).
         *
         * - If `false`, type declaration files are not generated.
         * - If a string is provided, it is used as the output folder path
         *   (relative to `process.cwd()`).
         * - If no value is provided, the default folder is `"docs/types"`.
         */
        typeDeclarationsFolder?: string | false;

        /**
         * If `true`, each component file will have auto-generated types at the end
         * of the file.
         *
         * These types are automatically generated based on the component's
         * definition to provide enhanced type safety and developer experience.
         *
         * This types are necessary for:
         *   - Auto complete properties, events, and methods in IDEs (specially in Lit templates).
         *   - Properly define the events of each custom element with type assertions for their event details.
         *   - Properly type the querySelectors used to find a custom element.
         */
        typesForComponents?: boolean;

        /**
         * If `true`, each component file will have auto-generated readme file
         * located in the same folder as the component
         *
         * These readmes provides a detailed documentation of the component's
         * properties, events, methods, CSS parts, CSS custom properties, and
         * any other relevant information that can be retrieved from the
         * component's definition.
         */
        readmesForComponents?: boolean;
      };

  /**
   * Paths or patterns to include when searching for components.
   *
   * If a path or pattern is specified, only components matching these will be
   * included.
   *
   * If some of these paths are also excluded by the `excludedPaths` option,
   * the exclusion takes precedence.
   *
   * @default `/\.lit\.(ts|js)$/`.
   */
  includedPaths?: RegExp | RegExp[];

  relativeComponentsSrcPath?: string;
};

export type KasstorBuildComponentData = {
  component: ComponentDefinition;
  fileContent: string;
  filePath: string;
};

